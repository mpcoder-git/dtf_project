<p>7.5 Работа с трендами</p>
<br><br>
<p>График изменения цены акции – основной инструмент любого участника рынка. 
Изучением графиков занимается целый раздел биржевой науки - технический анализ 
(technical analysis). Трудно переоценить его роль в выборе точек входа и выхода 
из позиции. Даже те, кто не пользуется его сигналами и моделями, прекрасно знают 
о них. Это азбука биржевика. Поэтому уметь «читать» график необходимо любому трейдеру.</p>
<br><br>
<p><b>7.1 Построение графика</b></p>
<br><br>
<p>Сейчас существует достаточно компьютерных программ для автоматического построения 
графиков цен акций, что сильно облегчает и ускоряет анализ. График строится в двух осях: 
временной (горизонтальной) и стоимостной (вертикальной).</p>
<p>Горизонтальная ось дискретна, т. е. поделена на равные отрезки времени, называемые периодами.</p>
<p>Для каждого периода по горизонтали выделяется 4 значения по вертикали:</p>
<ul>
	<li>Значение стоимости на начало периода (open price);</li>
	<li>Значение стоимости на окончание периода (close price);</li>
	<li>Высшее значение стоимости за период (high);</li>
	<li>Низшее значение стоимости за период (low).</li>
</ul>
<p>На их основе иногда рассчитываются среднее арифметическое цен открытия и закрытия периода, 
т.е. значения = 0,5*(open+close); среднее арифметическое высшей и низшей цены, т.е. значения = 0,5*(high+low) 
и некоторые другие. Одно или несколько из всех этих значений используются для построения графика.</p>
<p>Период для графика можно выбрать любой. Наиболее популярные и востребованные периоды для NYSE – 1 минута, 
5 минут, 10 минут, 15 минут, 30 минут, 1 день, 1 неделя, 1 месяц, 1 квартал, 1 год.</p>
<p>Несмотря на длительность выбранного рабочего периода, изучение графиков с периодами в 1 день, 1 неделю и 1 месяц 
является обязательным и необходимым для любого трейдера. Эти графики используют институциональные инвесторы, по ним 
ведется анализ и принятие решения относительно позиций в миллионы акций.</p>
<p>Поэтому видеть и понимать эту картину необходимо каждому. По таким графикам изучают глобальную тенденцию цены акции.</p>
<p>Выбор периода для основного рабочего графика зависит от того, на каком временном отрезке собирается торговать трейдер. 
Чем дольше планируемое время нахождения в позиции, тем больше период для построения графика, по которому трейдер будет 
отслеживать текущее состояние позиции. Внутридневные трейдеры выбирают периоды, равные 1, 5, 15 минутам. По таким графикам 
изучают характер торговли акции:</p>
<ul>
	<li>«быстрая» она или «медленная» (т. е. волатильность, скорость изменения цены);</li>
	<li>много ли на графике разрывов (т. е. ликвидность, наличие или отсутствие объема торгов).</li>
</ul>
<br><br>
<p><b>7.2 Типы графиков</b></p>
<br><br>
<p><b>Линейный график (line chart)</b></p>
<br><br>
<p>Самый простой и самый известный тип графика. Строится путем соединения прямыми отрезками точек одного из типов значений. 
Наиболее востребован линейный график по ценам закрытия периодов (close line). Все графики в СМИ относятся именно к этому типу.</p>
<br><br>
