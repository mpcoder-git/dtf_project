<br><br>
<p>Бары</p>
<p><b>Японские свечи (Japanese Candlesticks)</b></p>
<p>Тип графиков получил название соответственно стране изобретения. На данный момент большинство аналитиков и трейдеров, в том числе и западных, 
приняли данный способ отображения ценовых движений за основной.</p>
<p>По сути «свечи» аналогичны «барам», а если точнее - «барам» OHLC. Для их построения используются те же 4 ценовых уровня за каждый период. 
У свечи есть  «тело», которое образуется ценами открытия и закрытия, а также «тени» или «фитили», которые образуются максимальной и минимальной ценами.</p>
<p>Кроме того, у каждой свечи есть цвет, который показывает, выросла цена за период или упала: если цена закрытия периода ниже, чем цена открытия, 
то тело свечи закрашивается черным (или красным).</p>
<br><br>
<img class="centeredblock"  src="<?php echo PATCH_LESSONS."picture7_4.png"; ?>" alt="">
<br><br>
<p>Если цена закрытия периода выше, чем цена открытия, то тело свечи закрашивается белым (или зеленым).</p>
<br><br>
<img class="centeredblock"  src="<?php echo PATCH_LESSONS."picture7_5.png"; ?>" alt="">
<br><br>
<p><b>Преимущества этих графиков</b></p>	
<ul>
	<li>Они несут весь необходимый объем информации (4 ценовых уровня).</li>
	<li>Они наглядны, и на них приятнее смотреть, чем на частокол «баров» (за счет объемности «тела»).</li>
	<li>Скорость восприятия информации по таким графикам в целом выше (за счет использования цвета).</li>
	<li>Такие графики можно использовать не только для западного технического анализа, но и для богатого
	моделями и фигурами японского, чего нельзя сказать о других графиках.</li>
</ul>
<p>К относительному недостатку можно отнести тот факт, что из-за поперечного объема тела свечи график занимает больше места. 
Поэтому на экране и распечатке помещается информация о меньшем количестве периодов, чем на линейном.</p>
