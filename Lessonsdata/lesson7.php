<p><b>7.3 Тренды (trends)</b></p>
<br><br>
<p>Определение тренда было дано Чарльзом Доу, который считается основателем западного теханализа: 
при восходящем (нисходящем) тренде каждый последующий пик и каждый спад должен быть выше (ниже) предыдущего.</p>
