<br><br>
<p>Линейный график</p>
<p><b>«Бары» (bar chart)</b></p>
<p>Данные рассматриваемого показателя изображаются на графике в виде последовательной серии «баров» — вертикальных штрихов с 
горизонтальными черточками. Каждый «бар» строится по данным своего периода. Между собой на графике они не соединены.</p>
<p>В большинстве случаев каждый бар показывает максимальную, минимальную и последнюю цену (HLC – high, low, close). Применительно 
к рынку ценных бумаг некоторые бары изображают цену открытия, максимальную, минимальную цены и цену закрытия (OHLC – open, high, low, close).</p>
<br><br>
<img class="centeredblock"  src="<?php echo PATCH_LESSONS."picture7_2.png"; ?>" alt="">
<br><br>

<p>По сравнению с линейными, графики в виде «баров» менее приятны глазу, зато гораздо информативнее. Линейный график отображает только одну 
цену для каждого периода, «бары» - четыре, т. е. всю необходимую для анализа информацию.</p>
<p>«Бары» были придуманы западными техническими аналитиками, которым потребовался инструмент для более глубокого анализа графиков.</p>
