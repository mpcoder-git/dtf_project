<p>Японские свечи</p>
<p><b>Уровни поддержки и сопротивления (support&resistance)</b></p>
<p>Многолетний анализ графиков показывает, что на графиках цен существуют определенные уровни, которые 
можно использовать при торговле. Это уровни поддержки и сопротивления.</p>
<p>Визуально уровень поддержки / сопротивления выглядит как линия, ниже / выше которой цена не идет. 
Эта особенность графиков цен имеет объяснение. Уровни образуются в результате дисбаланса спроса и предложения на рынке.</p>
<p>Сопротивление — по определенной цене есть очень большой продавец или группа продавцов, либо слишком 
мало участников готово купить так дорого; чаще всего и то, и другое одновременно.</p>
<p>Поддержка — по определенной цене есть очень большой покупатель или группа покупателей, либо 
очень мало участников готово продать так дешево; чаще всего и то, и другое одновременно.</p>
<p>Линия, показывающая уровень, может быть горизонтальным или наклонным отрезком прямой или даже 
непрямой линией, построенной по четко описанному закону.</p>
