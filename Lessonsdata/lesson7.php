<br><br>
<p>Тренд - основополагающее понятие в биржевой торговле. Поиск и открытие позиции по тренду, т. е. 
в направлении движения акции, является основной и самой разумной торговой стратегией. Торговля
против тренда – занятие весьма опасное и в большинстве случаев бесполезное. Гораздо проще иметь 
ту позицию, с которой согласно большинство участников торгов, что и выражается в образовании устойчивого тренда.</p>
<p><b>Теория Доу</b></p>
<p>Теория, названная именем Доу, была разработана уже после его смерти тремя математиками. У нее есть шесть постулатов.</p>
<p><i>Постулат 1. Существуют три типа трендов.</i></p>
<p>Согласно теории Доу существуют три типа трендов: первичный (или долгосрочный), вторичный (или промежуточный) и малый (или краткосрочный).</p>
<p><i>Постулат 2. Каждый первичный тренд имеет три фазы.</i></p>
<p>Теория Доу утверждает, что каждый первичный тренд состоит из трех фаз: фазы накопления, фазы участия и фазы реализации. </p>
<p><i>Постулат 3. Фондовый рынок учитывает все новости.</i></p>
<p>Цены акций быстро реагируют на любую новую информацию. Это относится не только к финансовым и экономическим показателям, но и к любой новости вообще. </p>
<p><i>Постулат 4. Биржевые индексы должны быть согласованы.</i></p>
<p>Данное утверждение относится к промышленному индексу Доу-Джонса ($INDU) и транспортному индексу Доу-Джонса ($TRAN). Согласно теории Доу, 
текущий тренд и сигналы к смене тренда должны подтверждаться обоими индексами.</p>
<p><i>Постулат 5. Тренды подтверждаются объемами торгов.</i></p>
<p>Доу считал, что для распознавания тренда необходимо учитывать объемы торгов. </p>
<p>Изменение цен акций при малом объеме торгов может быть объяснено множеством различных причин и не характеризует текущий тренд. Если 
же изменение цены произошло на фоне больших объемов торгов, то это отражает «реальное» мнение рынка и характеризует развитие текущего тренда или новый тренд.</p>
<p><i>Постулат 6. Тренды действуют до тех пор, пока не появится однозначный сигнал о прекращении. </i></p>
<p>Это утверждение следует понимать следующим образом: тренд имеет тенденцию к продолжению. Изменения цен, не соответствующие тренду, 
в случае неуверенности необходимо интерпретировать как временную корректировку, а не как смену тренда.</p>
<p>Этот постулат является ключевым для теории теханализа. </p>
<p><b>Гистограммы объема</b></p>
<p>Объем торговли за период измеряется количеством акций, с которыми были совершены сделки в течение периода. Обычно объем изображается в 
виде гистограммы, т. е. вертикальных штрихов, поднимающихся от одного нулевого уровня. Чем больше объем, тем выше вертикальный штрих.</p>
<p>График объема отображается вместе с одним из перечисленных типов графиков (обычно в нижней части листа или окна с графиком) и используется 
при анализе ценового графика. Основное правило относительно объема – движения цены должны подтверждаться объемом.</p>
<p><b>Построение трендов</b></p>
<p>Теория Доу, которая подтверждается реальной биржевой торговлей, гласит, что тренд – основной инструмент в работе на рынке. Вопрос заключается в том, 
как этот тренд найти. Легче всего визуально убедиться в том, что рассматриваемый график цены действительно находится в тренде, можно, 
построив на графике уровни поддержки и/или сопротивления.</p>
<br><br>
<p><b>7.4 Скользящие средние (Moving Averages, MA)</b></p>
<br><br>
<p>Непрямые линии, дающие уровень поддержки при uptrend’e и уровень сопротивления при downtrend’e. Линии строятся путем соединения отрезками точек, 
рассчитываемых по данным определенного количества предыдущих периодов.</p>
<p>Число используемых при расчете периодов называется порядком скользящей средней. Самые популярные значения порядка — 10, 20, 50, 200.</p>
<p>Чаще всего для расчета берутся цены закрытия периодов. Есть два основных типа скользящих средних n-го порядка.</p>
<p>Простая (Simple Moving Average, SMA)– точка для каждого последующего периода рассчитывается по формуле «сумма цен закрытия n периодов»/n. 
Значение для очередной точки линии является арифметическим средним предыдущих n цен закрытия. Главная особенность SМА в том, что она реагирует 
на одно изменение цены два раза: при включении и исключении значения из расчета.</p>
<p>Экспоненциальная (Exponential Moving Average, EMA). Главная особенность ЕМА в том, что она включает в себя все цены предыдущего периода, 
а не только отрезок, заданный при установке периода. При этом более поздним значениям придается больший вес.</p>
<br><br>
<img class="centeredblock"  src="<?php echo PATCH_LESSONS."picture7_13.png"; ?>" alt="">
<br><br>
<p>Скользящие средние</p>
<p>Расчет точек и построение линии производится на компьютере с помощью программы отображения графиков автоматически.</p>
<p>Чем выше порядок средней, тем дальше она отстоит от графика, тем более долгосрочный тренд она ограничивает. MA с меньшим 
периодом называется «быстрой» или «короткой». MA с большим периодом называется «медленной» или «длинной».</p>
<br><br>
<p><b>7.5 Работа с трендами</b></p>
<br><br>
<p>Обнаруженный тренд с выявленными уровнями поддержки и сопротивления можно использовать при торговле. Однако следует сделать замечание.</p>
<p><b>Временной период</b></p>
<p>Тренды есть на любом временном периоде. Тренд можно найти и на дневном, и на минутном графике. Разумеется, не всегда они будут направлены в одну сторону.</p>
<p>Цена акции может расти годами и при этом падать в течение дня часами. Таким образом, на недельном графике будет явный uptrend, 
на 5-минутном – downtrend. Какой же тренд использовать при торговле?</p>
<p>Чем больше период тренда, т. е. период графика, на котором он явно прослеживается, тем он сильнее. Поэтому годовой тренд всегда 
преобладает над недельным, недельный — над дневным и так далее. Поэтому:</p>
<p>Цена всегда будет легче двигаться в сторону более долгосрочного тренда на отрезке времени, сопоставимом с величиной этого периода.</p>
<p>Иными словами, если на годовом графике uptrend, это не значит, что весь год необходимо покупать. Просто покупки в этом году должны принести больше прибыли.</p>
<p>Если цена акции находится возле одного из уровней долгосрочного тренда, то сигналы от краткосрочных трендов менее существенны.</p>
<p>Иными словами, если цена собирается отбиться или пробить уровень тренда, формировавшийся годами, тренд на минутном графике не следует использовать при принятии торгового решения.</p>