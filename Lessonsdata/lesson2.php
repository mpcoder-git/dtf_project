<p><b>2.6 Специалисты</b></p>
<br><br>
<img class="centeredblock" src="<?php echo PATCH_LESSONS."picture2_5.png"; ?>" alt="">
<br><br>
<p>На данный момент существует всего 7 фирм специалистов (NYSE Specialist Firm), в которых работают все специалисты на бирже:</p>
<br><br>
<p>1. Banc of America Specialist</p>
<p>2. Bear Wagner Specialists LLC</p>
<p>3. Kellogg Specialist Group</p>
<p>4. LaBranche & Co. LLC</p>
<p>5. Spear, Leeds & Kellogg Specialists LLC</p>
<p>6. SIG Specialists, Inc.</p>
<p>7. Van der Moolen Specialists, USA LLC</p>
<br><br>
<p>Роль специалиста на бирже огромна. Он исполняет все ордера от брокеров на полу, на его компьютер приходят все ордера с электронных систем, от самых маленьких до самых больших. 
Он раньше всех замечает дисбаланс покупателей и продавцов. Он первым видит громадные ордера, которые присылают институциональные инвесторы. От него зависит, как именно эти ордера будут исполняться. 
Таким образом, специалист всегда точно знает, куда пойдет акция в ближайшее время в течение дня. Статистика показывает, что доля участия специалистов во всех торговых операциях с акциями — около 10 процентов.</p>
<p>Важно помнить, однако, что специалист — наемный служащий. В процессе ведения акции (управления торговлей акцией) он использует капитал, предоставленный ему фирмой. 
От того, насколько профессионально он выполняет свои обязанности, зависит его заработок. Любого специалиста фирма может перевести на другие акции, если по какой-то 
причине его деятельность не устраивает руководство. На деятельность специалиста наложен ряд ограничений, за соблюдением которых следит фирма, в которой он работает, 
контрольный комитет биржи и даже SEC.</p>









