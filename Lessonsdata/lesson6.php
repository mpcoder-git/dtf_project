<p><b>6.1 Типы ордеров, их особенности и классификация </b></p>
<br><br>
<p>По назначению приказы могут быть «прямыми», «по исполнению» и «взаимозаменяемыми». Обсудим каждый из них: </p>
<p>1) Простой ордер представляет собой единичную заявку, которая необходима для входа в позицию или же выхода из нее.
 Ордер реализуется в тот момент, когда цена достигнет определенного уровня. </p>
<p>2) Взаимозаменяемые заявки «работают» в паре. Чаще всего они представляют собой сочетание «Тейк Профит» и «Стоп Лосс».
 Взаимозаменяемые ордера стоит выставлять при наличии открытой позиции. Это отличная возможность держать позицию под 
 контролем весь необходимый промежуток времени. </p>
<p>3) Заявка по исполнению представляет собой связку ордеров, в которой содержится простой ордер (играет роль открывающего)
 и пара взаимозаменяемых приказов. </p>
<p>Особенность заявок заключается в том, что они не ограничены во времени. То есть можно установить ордер и ожидать его 
исполнения на рынке. При желании заявку в любой момент можно отменить. Главное – не забывать снимать ордера после закрытия позиций. </p>
<p>Можно выделить три основных вида действий на бирже: </p> 
<p>1) Покупка акций. Делается это для дальнейшей перепродажи ценной бумаги и получения прибыли с разницы. Данную сделку называют «Buy»; </p> 
<p>2) Продажа ценной бумаги в долг. В этом случае происходит продажа оформленной в долг у брокера акции с расчетом, что через некоторое 
время тот сможет приобрести ее по более низкой цене и вернуть задолженность. Такая сделка носит название «Sell Short»; </p> 
<p>3) Продажа купленной акции (сделка осуществляется заранее). В этом случае акция продается через некоторый промежуток времени, 
но по более высокой стоимости.</p>
<p>Какое бы действие на рынке вы не осуществляли, будь это открытие или закрытие позиции, покупка или продажи акции, вы будете 
вынуждены подавать заявки, так называемые ордера.</p> 
<p>Ниже приведены типы ордеров, которые используются на бирже.</p>
<p>- Limit Order (лимитный ордер);</p>
<p>- Market Order (рыночный ордер);</p>
<p>- Stop market (стоп-маркет);</p>
<p>- Stop Limit Order (стоп-лимитный ордер);</p>
<p>- Limit-on-Open (LOO);</p>
<p>- Market-on-Open (MOO);</p>
<p>- Market-on-Close (MOC).</p>
<p>В процессе торговли мы будем пользоваться следующими типами ордеров: Limit order; Market order; Stop order. Другие типы ордеров 
мы использовать не будем.</p>
