<p><b>9.5 Анализ секторов</b></p>
<br><br>
<p>Деление компаний на сектора позволяет использовать стратегии, не применимые к отдельным акциям. Технически 
все такие стратегии одинаковы:</p>
<p>1. Трейдер находит торговую идею для какого-либо сектора.</p>
<p>2. Соответственно торговой идее и состоянию рынка в целом выбирается позиция.</p>
<p>3. Из сектора выбирается набор перспективных компаний, анализ акций которых не противоречит выбранной позиции.</p>
<p>4. Позиция открывается не по одной акции, а по корзине (basket), то есть по выбранному набору акций.</p>
<p>5. Убыточные позиции в корзине закрываются на определенном уровне, прибыльные сохраняются, иногда увеличиваются.</p>
<p>За всеми акциями корзины следить невозможно. Поэтому для входа и выхода из позиций как раз и пригодится анализ индекса сектора.</p>
<p>Основной принцип таких стратегий строится на том, что если изначальный анализ верен и позиция по сектору 
выбрана правильно, то прибыльных позиций будет больше, нежели убыточных. Таким образом, неизбежные при таком 
способе убытки, связанные не в последнюю очередь с невозможностью контроля всех позиций, будут с лихвой покрыты прибылями.</p>
<p>Основные причины для торговли сектора таковы:</p>
<p><b>1. Существенное расхождение направления рынка и тенденции сектора.</b></p>
<p>Ни одна акция или даже сектор не может длительное время противостоять устойчивому тренду на рынке. 
Чем сильнее расхождение, тем сильнее затем обратное движение, которое как бы «догоняет» рынок.</p>
<p><b>2. Сильные новости на сектор (отчеты, имеющие влияние на данный сектор; </b> изменения законодательства; постановления 
комиссий по контролю за компаниями этого сектора; изменения рейтинга брокерских компаний и т. д.).</p>
<p>Серьезные новости могут вынудить крупных инвесторов сократить или нарастить позицию по акциям. А фонды торгуют 
в основном портфелями, в том числе и с учетом деления по секторам.</p>
<p><b>3. Изменения цен на сырье.</b></p>
<p>Эту причину стоит выделить отдельно, хотя в целом она аналогична предыдущей. На рынке есть ряд 
секторов, которые практически полностью зависят от одного показателя — цены на 
соответствующее сырье, т. е. от изменений цен на это сырье на товарных биржах. Это в первую очередь 
нефтедобывающая, сталелитейная и золотодобывающая промышленности.</p>
<p>Для этих секторов оговорка о направлении рынка может не иметь никакого смысла. При достаточно сильных новостях на товарных 
биржах (природные катаклизмы, забастовки рабочих, смены политического строя крупнейших добывающих стран и т. д.) цены на 
сырье меняются независимо от общего экономического фона.</p>
<p>Цены акций компаний в этих секторах будут следовать за ценами соответствующего сырья даже вопреки тенденции всего рынка. 
Поэтому для торговли таких акций от трейдера требуются особое внимание, глубокий анализ и концентрация на данном секторе и только на нем.</p>
<p><b>4. Сильные новости на market mover’a.</b></p>
<p>Market mover — крупная, известная компания. Финансовое положение ее акционеров, доходы ее многочисленных партнеров, уровень 
конкуренции в отрасли, приток капитала в сектор, стабильность всей экономики в целом — на все это может повлиять новость на 
такую компанию.</p>
<p>Особенность перечисленных новостей заключается в том, что они вынуждают принимать решения не только профессионалов, но и 
рядовых одиночных инвесторов. Чем известнее компания, тем популярнее инвестиции в ее акции. И тем больше непрофессиональных 
акционеров, которые не в состоянии провести глубокий анализ. Такое положение вещей часто способствует как развитию паники на 
рынке после выхода плохих новостей, так и эйфории после хороших.</p>
<p>Другая неочевидная особенность заключается в том, что новости одинаково действуют как на акции крупных компаний, так и на акции их прямых 
конкурентов. Сектор реагирует слаженно, хотя на первый взгляд кажется, что плохая новость должна поднять конкурентов и наоборот. Практика 
показывает, что в большинстве случаев это не так.</p>
<p><b>5. Отстающие акции.</b></p>
<p>По ряду причин цена какой-либо акции из сектора, который совершает сильное движение, может не изменяться. Однако, чем серьезнее 
движение в секторе, тем выше вероятность того, что задержка временная. Через некоторое время цена изменится в ожидаемом направлении, 
«догонит» сектор. Поиск таких акций и своевременное получение позиции по ним является одной из самых безопасных стратегий, в отличие 
от попыток «догнать» акции, цены которых уже существенно изменились.</p>
<p>По этой причине крайне важно хорошо знать большинство акций на рынке в целом, которые имеют потенциал движения, и отлично знать все 
такие акции сектора, который выделен на сегодня в качестве перспективного. Дело в том, что акции
«догоняют» сектор достаточно быстро, и поиск с нуля отнимет слишком много времени. Момент для получения позиции будет утерян.
</p>
<p><b>6. Технические пробои индекса сектора при нейтральном или поддерживающем направлении рынка.</b></p>
<p>Технический анализ гласит, что все данные о компании заложены в цену акции. По ценам ряда акций 
рассчитывается индекс сектора. Поэтому можно предположить, что выявленный с помощью теханализа пробой 
на графике индекса сектора будет соответствовать положению дел в реальности, то есть экономическому 
положению компаний сектора. Таким образом, увидев подобную ситуацию, масса инвесторов примет соответствующее решение.</p>
<p>Утверждение, безусловно, спорное. Оставив размышления о причинно- следственных связях, можно просто использовать 
эти сигналы для покупки и продажи акций. Практика показывает, что это имеет смысл.</p>
<p>В биржевой торговле вообще много парадоксов. Некоторые из них с трудом поддаются рациональному объяснению. Вопрос 
в другом: зачем человек приходит на биржу? Изучать рынок или зарабатывать деньги? От ответа на этот вопрос зависит 
отношение к подобным парадоксам. Можно пытаться их разрешить, можно использовать отмеченные закономерности для 
принятия торговых решений. В первом случае можно получить признание, во втором — заработать. Каждый выбирает для себя сам.</p>






